// Write a Program to perform all arithmetic operators such as +, -, *, / and %
#include "stdio.h"

int main()
{
    int a;
    int b;
    a = 36;
    b = 5;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b);
    printf("a * b = %d\n", a * b);
}